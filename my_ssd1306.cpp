// init according to SSD1306 data sheet
void  initSSD1306(void) {

	//init_I2C0();  //  C8 (SCL) C9 (SDA) are the oled
     i2c_init();
    // Init sequence for 128x64 OLED module
    ssd1306_command(SSD1306_DISPLAYOFF);                    // 0xAE

    ssd1306_command(SSD1306_SETDISPLAYCLOCKDIV);            // 0xD5
    ssd1306_command(0x80);                 // the suggested ratio 0x80
   
    ssd1306_command(SSD1306_SETMULTIPLEX);                  // 0xA8
    ssd1306_command(0x3F);
   
    ssd1306_command(SSD1306_SETDISPLAYOFFSET);              // 0xD3
    ssd1306_command(0x0);                                   // no offset
   
    ssd1306_command(SSD1306_SETSTARTLINE);// | 0x0);        // line #0
   
    ssd1306_command(SSD1306_CHARGEPUMP);                    // 0x8D
    ssd1306_command(0x14);  // using internal VCC
   
    ssd1306_command(SSD1306_MEMORYMODE);                    // 0x20
    ssd1306_command(0x00);          // 0x00 horizontal addressing
   
    ssd1306_command(SSD1306_SEGREMAP | 0x1); // rotate screen 180
   
    ssd1306_command(SSD1306_COMSCANDEC); // rotate screen 180
   
    ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
    ssd1306_command(0x12);
   
    ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81
    ssd1306_command(0xCF);
   
    ssd1306_command(SSD1306_SETPRECHARGE);                  // 0xd9
    ssd1306_command(0xF1);
   
    ssd1306_command(SSD1306_SETVCOMDETECT);                 // 0xDB
    ssd1306_command(0x40);
   
    ssd1306_command(SSD1306_DISPLAYALLON_RESUME);           // 0xA4
   
    ssd1306_command(SSD1306_NORMALDISPLAY);                 // 0xA6
 
    ssd1306_command(SSD1306_DISPLAYON);                     //switch on OLED

}
 
void writeString(uint8_t *c) {
    while (*c) {
        printChar(*c);
        c++;
	}
}

void printHexByte(uint8_t num) {
	uint8_t temp = 0;
	temp = num>>4;
	if (temp<10)
        printChar(temp+'0');
	else 
        printChar(temp+'7');  //  or 'A'-10
	temp = 0x0f&num;
	if(temp<10)
        printChar(temp+'0');
	else 
        printChar(temp+'7');
}

void printHexWord(uint16_t num) {
	uint8_t temp=0;
	temp = num>>8;
	printHexByte(temp);
	temp = num&0x00ff;
	printHexByte(temp);
}

void printMem(uint16_t loc) {
	uint16_t temp1;
	temp1 = *(uint16_t *)loc; //prints out the location = value
    printHexWord(temp1);
	return;
}

